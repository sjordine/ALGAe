package algae.philogeny.matrix;

/**
 *
 * @author sergio
 */
public class DistanceInfo {
    
    private DistanceType type;
    private double maxScore;
    private double similarityRate;


    /**
     * @return the type
     */
    public DistanceType getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(DistanceType type) {
        this.type = type;
    }

    /**
     * @return the maxScore
     */
    public double getMaxScore() {
        return maxScore;
    }

    /**
     * @param maxScore the maxScore to set
     */
    public void setMaxScore(double maxScore) {
        this.maxScore = maxScore;
    }


    /**
     * @return the similarityRate
     */
    public double getSimilarityRate() {
        return similarityRate;
    }

    /**
     * @param similarityRate the similarityRate to set
     */
    public void setSimilarityRate(double similarityRate) {
        this.similarityRate = similarityRate;
    }

}
