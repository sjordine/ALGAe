package algae.staralignment;

import algae.philogeny.matrix.DistanceType;

/**
 *
 * @author sergio
 */
public class AlignPair {

    private int anchor;
    private int newSequence;
    private DistanceType type;

    /**
     * @return the anchar
     */
    public int getAnchor() {
        return anchor;
    }

    /**
     * @param anchar the anchar to set
     */
    public void setAnchor(int anchor) {
        this.anchor = anchor;
    }

    /**
     * @return the newSequence
     */
    public int getNewSequence() {
        return newSequence;
    }

    /**
     * @param newSequence the newSequence to set
     */
    public void setNewSequence(int newSequence) {
        this.newSequence = newSequence;
    }

    /**
     * @return the type
     */
    public DistanceType getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(DistanceType type) {
        this.type = type;
    }

}
